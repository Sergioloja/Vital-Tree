/**
 *  @swagger
 *  /api/corporate_sponsors:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Corporate_sponsors]
 *      summary: Get all corporate_sponsors
 *      description: Get all corporate_sponsors
 *      responses:
 *        200:
 *          description: Corporate_sponsors list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Corporate_sponsors"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const globalAccess = req.currentUser.app_role.globalAccess;

    const payload = await Corporate_sponsorsDBApi.findAll(
      req.query,
      globalAccess,
    );
    if (filetype && filetype === 'csv') {
      const fields = ['id', 'company_name', 'industry', 'csr_focus'];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

